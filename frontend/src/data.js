export const formatProducts = (rawData) => {
  const categories = { "상의": [], "바지": [], "아우터": [], "신발": [] };
  
  // 데이터가 배열인지 확인하는 안전장치 추가
  if (!Array.isArray(rawData)) return {};

  rawData.forEach(item => {
    if (categories[item.upper_category]) {
      categories[item.upper_category].push(item.img_url);
    }
  });

  const formatted = {};
  personas.forEach(p => {
    formatted[p] = categories;
  });
  
  return formatted;
};